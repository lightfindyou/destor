void getV5(unsigned char* d, int** V){
    for(int i = 0; i<5; i++){
        char c = d[i];
        int* t = V[i];
        t[0] = (0x1 & c)?1:(-1);
        t[1] = (0x2 & c)?1:(-1);
        t[2] = (0x4 & c)?1:(-1);
        t[3] = (0x8 & c)?1:(-1);
        t[4] = (0x10 & c)?1:(-1);
        t[5] = (0x20 & c)?1:(-1);
        t[6] = (0x40 & c)?1:(-1);
        t[7] = (0x80 & c)?1:(-1);
    }
}

